    private void eliminarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarButtonActionPerformed
        Categoria categoriaSeleccionada = (Categoria) seleccionarCategoriaComboBox.getSelectedItem();
        
        if (categoriaSeleccionada != null) {
            int confirmacion = JOptionPane.showConfirmDialog(
                this, 
                "¿Está seguro que desea eliminar la categoría: " + categoriaSeleccionada.getNombre() + "?", 
                "Confirmar eliminación", 
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);
            
            if (confirmacion == JOptionPane.YES_OPTION) {
                boolean eliminado = categoriaController.eliminarCategoria(categoriaSeleccionada.getId());
                
                if (eliminado) {
                    JOptionPane.showMessageDialog(this, 
                        "Categoría eliminada exitosamente", 
                        "Éxito", 
                        JOptionPane.INFORMATION_MESSAGE);
                    cargarCategorias(); // Recargar la lista
                } else {
                    JOptionPane.showMessageDialog(this, 
                        "Error al eliminar la categoría", 
                        "Error", 
                        JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_eliminarButtonActionPerformed


    private void cargarCategorias() {
        ArrayList<Categoria> categorias = categoriaController.obtenerCategorias();
        DefaultComboBoxModel<Categoria> modelo = new DefaultComboBoxModel<>();
        
        for (Categoria categoria : categorias) {
            modelo.addElement(categoria);
        }
        
        seleccionarCategoriaComboBox.setModel(modelo);
        
        if (categorias.isEmpty()) {
            eliminarButton.setEnabled(false);
            JOptionPane.showMessageDialog(this, 
                "No hay categorías disponibles para eliminar", 
                "Información", 
                JOptionPane.INFORMATION_MESSAGE);
        }
    }

